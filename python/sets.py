# Set Variable
my_set = {"January", "February", "March"}

for element in my_set:
    print(element)

# Add Data to Set
my_set.add("April")
print(my_set)

# Remove Data from a Set
my_set.remove("January")
print(my_set)

# List Variable
my_list = ["January", "February", "March"]

# Remove Data from a List
my_list.remove("January")
print(my_list)
